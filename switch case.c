#include<stdio.h>
int main()
{
    int Digit;
    printf("Enter a Digit:");
    scanf("%d",&Digit);
    switch(Digit)
    {
        case 1:
        printf("one");
         break;
        case 2:
        printf("Two");
        break;

        case 3:
        printf("three");
break;
        case 4:
        printf("Four");
break;

        case 5:
        printf("Five");

        return 0;

    }

}
